function toggleMode() {
       
    let html = document.documentElement

    if(html.classList.contains("root")) {
        html.classList.remove("zoro")
        html.classList.remove("sajin")
        html.classList.remove("nami")
        html.classList.remove("usopp")
        html.classList.remove("chopper")
        html.classList.remove("robin")
        html.classList.remove("franky")
        html.classList.remove("brook")
        html.classList.remove("jinbe")
    } else {
        html.classList.add("root")
    }
}

